package com.cs336.pkg;

public class Shirts {
   int itemID;
   String shirtSize;
   String shirtGender;
   String shirtBrand;
   String color;
   double bidPrice;

   public int getItemID() {
      return this.itemID;
   }

   public void setItemID(int itemID) {
      this.itemID = itemID;
   }

   public String getShirtSize() {
      return this.shirtSize;
   }

   public void setShirtSize(String shirtSize) {
      this.shirtSize = shirtSize;
   }

   public String getShirtGender() {
      return this.shirtGender;
   }

   public void setShirtGender(String shirtGender) {
      this.shirtGender = shirtGender;
   }

   public String getShirtBrand() {
      return this.shirtBrand;
   }

   public void setShirtBrand(String shirtBrand) {
      this.shirtBrand = shirtBrand;
   }

   public String getColor() {
      return this.color;
   }

   public void setColor(String color) {
      this.color = color;
   }

   public double getBidPrice() {
      return this.bidPrice;
   }

   public void setBidPrice(double bidPrice) {
      this.bidPrice = bidPrice;
   }
}
